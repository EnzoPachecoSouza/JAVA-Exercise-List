public class OperadoresLogicos {
	
	public static void main (String[] args) {
		int a = 1;
		int b = 2;
		int c = 3;
		int d = 4;
		System.out.println(a > b || c < d);
		System.out.println(a > b || c < d);
		System.out.println(a > b && c < d);
		System.out.println(a > b & c < d); 
		System.out.println(a > b | c < d); 
	}
}
//Com 2 operadores ele analísa só o primeiro termo
// Com 1 operador ele analísa todas informações

