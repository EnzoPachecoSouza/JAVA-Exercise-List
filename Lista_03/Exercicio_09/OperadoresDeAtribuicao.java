public class OperadoresDeAtribuicao {
	
	public static void main (String[] args) {
		int a = 1;
		System.out.println(a);
		
		a += 2;
		System.out.println(a);
		
		a -= 1;
		System.out.println(a);
		
		a *=3;
		System.out.println(a);
		
		a /=2;
		System.out.println(a);
		
		a /=2;
		System.out.println(a);
		
		a ++;
		System.out.println(a);
		
		a --;
		System.out.println(a);
		
		//A cada passo o resultado de (a), ia mudando de acordo com cada operação e número que estivesse na linha de código. 
		//(O SINAL SEMPRE DEVE ESTAR JUNTO DO IGUAL, PARA DAR CONTINUIDADE).
	}
}

