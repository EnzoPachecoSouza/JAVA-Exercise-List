public class TestaTiposPrimitivos {
	
	public static void main (String[] args) {
		System.out.println("Existem 3 tipos de variaveis no tipo primitivo, sendo elas: NUMERICA, CARACTER E BOOLEANOS \n");
		
		byte n1 = 110;
		System.out.println("byte ---> " + n1);
		
		short n2 = 25750;
		System.out.println("short ---> " + n2);
		
		int n3 = 1500750;
		System.out.println("int ---> " + n3);
		
		long n4 = 12345678910l;
		System.out.println("long ---> " + n4);
		
		float n5 = 3.163820404f;
		System.out.println("float ---> " + n5);
		
		double n6 = 102.8948584;
		System.out.println("double ---> " + n6);
		
		char vogal = 'A';
		System.out.println("char ---> " + vogal);
		
		char consoante = 'Z';
		System.out.println("char ---> " + consoante);
		
		boolean verdade = true;
		System.out.println("boolean ---> " + verdade);
		
		boolean falso = false;
		System.out.println("boolean ---> " + falso);
	}
}

